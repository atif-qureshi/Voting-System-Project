public class Candidate_Data {
    String constitution, pti, ptisymbol, pmln, pmlnsymbol, ppp, pppsymbol;

    public Candidate_Data(String constitution, String pti, String ptisymbol, String pmln, String pmlnsymbol, String ppp, String pppsymbol) {
        this.constitution = constitution;
        this.pti = pti;
        this.ptisymbol = ptisymbol;
        this.pmln = pmln;
        this.pmlnsymbol = pmlnsymbol;
        this.ppp = ppp;
        this.pppsymbol = pppsymbol;
    }
}

