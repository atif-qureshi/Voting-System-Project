public class Person {
    public String name, cnic, city, place, area, constitution;
    public int id;

    public Person(String name, String cnic, String city, String place, String area, String constitution, int id) {
        this.name = name;
        this.cnic = cnic;
        this.city = city;
        this.place = place;
        this.area = area;
        this.constitution = constitution;
        this.id = id;
    }
}
